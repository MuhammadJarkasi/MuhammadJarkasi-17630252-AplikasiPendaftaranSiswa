    void cari() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("Nisn");
        tbl.addColumn("Nama");
        tbl.addColumn("Jenis Kelamin");
        tbl.addColumn("Alamat");
        tbl.addColumn("Tempat, Tanggal Lahir");
        tbl.addColumn("Asal Sekolah");
        tbl.addColumn("Tahun Lulusan");
        try{
            String sql ="SELECT * FROM tb_sma WHERE nisn like '%" + tCari.getText() + "%'";
            Connection con =(Connection) koneksi.getConnection();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            while (rs.next()){
                tbl.addRow(new Object[] {
                     rs.getString("nisn"),
                    rs.getString("nama"),
                    rs.getString("jkel"),
                    rs.getString("alamat"),
                    rs.getString("ttl"),
                    rs.getString("asalsekolah"),
                    rs.getString("tahunlulusan")
                });
                jTable1.setModel(tbl);
            }
        }catch (Exception e) {
            
        }
    }
    
