    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        
        String jk = null;
        
        if(rCowo.isSelected()){
            
            jk ="Laki-Laki";
        }else if(rCewe.isSelected()) {
            jk = "Perempuan";
        }
        
        try {
            String sql = "INSERT INTO tb_sma VALUES ('" + tNisn.getText() + "','"
                    + tNama.getText() +"','" + jk +"','" + tAlamat.getText() + "','" + tTtl.getText() +"','" + tSekolah.getText() + "','" + tTahun.getText() + "')";
            Connection con =(Connection) koneksi.getConnection();
            PreparedStatement pst = con.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Berhasil Menyimpan");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal Menyimpan");
        }
    }//GEN-LAST:event_jButton1MouseClicked

